# Range arguments (start, stop, step)
print(range(5, 10, 1))

# for loop using range()
for each in range(1,10):
	print(each)

# for loop using a list
fruits = ["apple", "banana", "mango", "orange"]
for each in fruits:
	print(each)

# for loop using a string
str = "coder academy"
for each in str:
	print(each)

# for loop using len
for each in range(len(fruits)):
	print(f"Index {each}: {fruits[each]}")